import argparse

from pathlib import Path
from typing import List, Optional


def build_parser(argv: Optional[List] = None):
    """Return ArgumentParser parser for script."""
    # Create parser object
    parser = argparse.ArgumentParser(
        prog="cazy_matrix",
        description="Build CAZy family presence/absence or frequency matrix",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )

    # Add positional arguments to parser

    parser.add_argument(
        "alpha_beta_list",
        type=Path,
        help="Path to alpha-beta tab deliminted list used as input for coinfinder",
    )

    parser.add_argument(
        "-c",
        "--cf_matrix",
        dest="cf_matrix",
        default=None,
        help="Path to write out CAZy fam frequency matrix to",
    )

    parser.add_argument(
        "-p",
        "--pa_matrix",
        dest="pa_matrix",
        default=None,
        help="Path to write out presence-absence matrix to",
    )

    # Add option to force file over writting
    parser.add_argument(
        "-f",
        "--force",
        dest="force",
        action="store_true",
        default=False,
        help="Force file over writting",
    )

    # Add option to specific directory for log to be written out to
    parser.add_argument(
        "-l",
        "--log",
        type=Path,
        metavar="log file name",
        default=None,
        help="Defines log file name and/or path",
    )

    # Add option to specify verbose logging
    parser.add_argument(
        "-v",
        "--verbose",
        dest="verbose",
        action="store_true",
        default=False,
        help="Set logger level to 'INFO'",
    )

    if argv is None:
        # parse command-line
        return parser
    else:
        # return namespace
        return parser.parse_args(argv)

